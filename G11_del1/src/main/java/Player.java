public class Player {
    private String name;
    private int score;
    public Player(){
        String name;
        Integer score;

    }
    public void setPlayerName(String String){
        name = String;
    }
    public void setPlayerScore(Integer Int){
        score = Int;
    }
    public String getPlayerName(){
        return name;
    }
    public Integer getPlayerScore(){
        return score;
    }
}
